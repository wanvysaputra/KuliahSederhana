    private void tblMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMahasiswaMouseClicked
        // TODO add your handling code here:
        int baris = tblMahasiswa.rowAtPoint(evt.getPoint());
        String nim =tblMahasiswa.getValueAt(baris, 1).toString();
        String nama = tblMahasiswa.getValueAt(baris,2).toString();
        String prodi = tblMahasiswa.getValueAt(baris, 3).toString();
        String alamat=tblMahasiswa.getValueAt(baris, 4).toString();
        String email = tblMahasiswa.getValueAt(baris, 5).toString();        
        txtNim.setText(nim);       
        txtNama.setText(nama);
        cbProdi.setSelectedItem(prodi);        
        txtAlamat.setText(alamat);        
        txtEmail.setText(email);
        txtNama.requestFocus();
    }//GEN-LAST:event_tblMahasiswaMouseClicked

    private void btTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTambahActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO t_mhs VALUES ('"+txtNim.getText()+
                    "','"+txtNama.getText()+
                    "','"+cbProdi.getSelectedItem()+
                    "','"+txtAlamat.getText()+
                    "','"+txtEmail.getText()+"')";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            writeLog("Penyimpanan Data Berhasil dengan NIM "+txtNim.getText());
            load_table();
            bersihkan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            writeLog("Data gagal disimpan : " + e.getMessage());
        }
    }//GEN-LAST:event_btTambahActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
        try {
            if ("".equals(txtNim.getText())){
                JOptionPane.showMessageDialog(this, "Isikan NIM terlebih dahulu");
            }else{
                String sql ="UPDATE t_mhs SET nama = '"+ txtNama.getText()+                    
                        "', prodi = '"+ cbProdi.getSelectedItem()+
                        "', alamat= '"+txtAlamat.getText()+
                        "', email= '"+txtEmail.getText()+
                        "' WHERE nim = '"+txtNim.getText()+"'";
                java.sql.Connection conn=(Connection)Config.configDB();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diperbaharui dengan NIM"+txtNim.getText());
                writeLog("Data Berhasil Diperbaharui dengan NIM "+txtNim.getText());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
            writeLog("Perubahan Data Gagal : " + e.getMessage());
        }
        load_table();
        bersihkan();

    }//GEN-LAST:event_btEditActionPerformed

    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        // TODO add your handling code here:
        try {
            if ("".equals(txtNim.getText())){
                JOptionPane.showMessageDialog(this, "Isikan NIM terlebih dahulu");
            }else{
                String sql ="DELETE FROM t_mhs WHERE nim='"+ txtNim.getText()+"'";
                java.sql.Connection conn=(Connection)Config.configDB();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus dengan NIM "+txtNim.getText());
                writeLog("Data Berhasil Dihapus dengan NIM "+txtNim.getText());
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            writeLog("Data gagal dihapus : " + e.getMessage());
        }
        load_table();
        bersihkan();
    }//GEN-LAST:event_btHapusActionPerformed

